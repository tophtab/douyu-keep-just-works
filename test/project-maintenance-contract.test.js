  const responsiveStyles = readRepoFile('src/docker/webui/styles-responsive.css')

  assert.match(packageJson.scripts?.['build:docker'], /cp -R src\/docker\/webui build\/docker\/docker\/webui/)
  assert.match(webui, /WEBUI_TEMPLATE_PATH = path\.join\(__dirname, 'webui', 'index\.html'\)/)
  assert.match(webui, /path\.join\(__dirname, 'webui', 'styles\.css'\)/)
  assert.match(webui, /path\.join\(__dirname, 'webui', 'styles-components\.css'\)/)
  assert.match(webui, /path\.join\(__dirname, 'webui', 'styles-tables\.css'\)/)
  assert.match(webui, /path\.join\(__dirname, 'webui', 'styles-responsive\.css'\)/)
  assert.match(webui, /WEBUI_STYLE_PATHS\.map\(readTextFile\)\.join\('\\n'\)/)
  assert.match(webui, /path\.join\(__dirname, 'webui', 'app-data\.js'\)/)
  assert.match(webui, /path\.join\(__dirname, 'webui', 'app-routing\.js'\)/)
  assert.match(webui, /path\.join\(__dirname, 'webui', 'app-dom\.js'\)/)
  assert.match(webui, /path\.join\(__dirname, 'webui', 'app-render\.js'\)/)
  assert.match(webui, /path\.join\(__dirname, 'webui', 'app-pages\.js'\)/)
  assert.match(webui, /path\.join\(__dirname, 'webui', 'app-actions\.js'\)/)
  assert.match(webui, /path\.join\(__dirname, 'webui', 'app-task-actions\.js'\)/)
  assert.match(webui, /path\.join\(__dirname, 'webui', 'app\.js'\)/)
