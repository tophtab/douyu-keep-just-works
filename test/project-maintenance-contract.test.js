  assert.match(webui, /replaceToken\(html, '__WEBUI_STYLES__', readStyles\(\)\)/)
  assert.match(webui, /replaceToken\(html, '__WEBUI_SCRIPT__', readScript\(\)\)/)
  assert.match(webui, /replaceToken\(html, '__DOCKER_WEBUI_PAGE_ROUTES_JSON__', JSON\.stringify\(DOCKER_WEBUI_PAGE_ROUTES\)\)/)

  assert.match(indexHtml, /__WEBUI_STYLES__/)
  assert.match(indexHtml, /__WEBUI_SCRIPT__/)
  assert.match(app, /window\.DOUYU_KEEP_WEBUI_DATA/)
  assert.match(app, /window\.DOUYU_KEEP_WEBUI_ROUTING/)
  assert.match(app, /window\.DOUYU_KEEP_WEBUI_DOM/)
  assert.match(app, /window\.DOUYU_KEEP_WEBUI_RENDER/)
  assert.match(app, /window\.DOUYU_KEEP_WEBUI_PAGES/)
  assert.match(app, /window\.DOUYU_KEEP_WEBUI_ACTIONS/)
  assert.match(app, /window\.DOUYU_KEEP_WEBUI_TASK_ACTIONS/)
  assert.match(appData, /window\.DOUYU_KEEP_WEBUI_DATA/)
  assert.match(appRouting, /__DOCKER_WEBUI_PAGE_ROUTES_JSON__/)
  assert.match(appRouting, /window\.DOUYU_KEEP_WEBUI_ROUTING/)
  assert.match(appDom, /window\.DOUYU_KEEP_WEBUI_DOM/)
  assert.match(appRender, /window\.DOUYU_KEEP_WEBUI_RENDER/)
  assert.match(appRender, /buildFansStatusTable/)
